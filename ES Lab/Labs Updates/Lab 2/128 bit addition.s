	AREA reset, DATA, READONLY
	EXPORT __Vectors
__Vectors
	DCD 0X10001000 
	DCD Reset_Handler
	ALIGN
	AREA mycode, CODE, READONLY
	EXPORT Reset_Handler
	ENTRY
Reset_Handler
	LDR R0,=N1
	LDR R1,[R0],#04
	LDR R6,=N2
	LDR R2,[R6],#04
	MOV R3,#04
	LDR R5,=RES
LOOP
	ADDS R4,R1
	ADDS R4,R2
	STR R4,[R5],#04
	LDR R1,[R0],#04
	LDR R2,[R6],#04
	MOV R4,#0
	ADCS R4,#0X0
	SUBS R3,#01
	BNE LOOP
	STR R4,[R5]
STOP
	B STOP
N1 DCD 0xD87FC5A7,0x8D33CD38,0x9FAB89A2,0xA257C901
N2 DCD 0xAFCB6BFD,0x3992F81C,0xC9E69770,0x66ECCC79
	AREA DATASEG, DATA, READWRITE
RES DCD 0
	END