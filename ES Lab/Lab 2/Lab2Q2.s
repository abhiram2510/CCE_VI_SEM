	AREA reset,DATA,READONLY
	EXPORT __Vectors
__Vectors
	DCD 0x10001000
	DCD Reset_Handler
	ALIGN
	AREA mycode, CODE, READONLY
	EXPORT Reset_Handler
	ENTRY
Reset_Handler
	LDR R0,=SRC+(N-1)*4
	LDR R1,=SRC1+(N-1)*4
	LDR R2,=DST
	LDR R3,=0x00000000
	MOV R4,#4
LOOP
	ADCS R3,R0,R1
	

STOP
	B STOP
N EQU 4
SRC DCD 0x00000010,0x00000011,0x00000012,0x00000013
SRC1 DCD 0x00000020,0x00000021,0x00000022,0x00000023
	AREA data, DATA, READWRITE
DST DCD 0x0
	END